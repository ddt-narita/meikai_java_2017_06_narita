package e_07_05;

public class Hello {

	public static void main(String[] args) {

		//挨拶のメソッドhelloを呼び出して「こんにちは。」と表示
		hello();
	}

	//挨拶して改行するメソッド
	static void hello() {
		//「こんにちは。」と表示して開業する
		System.out.println("「こんにちは。」");
	}

}
